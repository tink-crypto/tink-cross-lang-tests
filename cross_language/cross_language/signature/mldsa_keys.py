def _basic_p65_key() -> ml_dsa_pb2.MlDsaPrivateKey:
  return ml_dsa_pb2.MlDsaPrivateKey(
      version=0,
      key_value=_seed(),
      public_key=ml_dsa_pb2.MlDsaPublicKey(
          version=0,
          params=ml_dsa_pb2.MlDsaParams(ml_dsa_instance=ml_dsa_pb2.ML_DSA_65),
          key_value=_mldsa65_public_value(),
      ),
  )


def _basic_p87_key() -> ml_dsa_pb2.MlDsaPrivateKey:
  return ml_dsa_pb2.MlDsaPrivateKey(
      version=0,
      key_value=_seed(),
      public_key=ml_dsa_pb2.MlDsaPublicKey(
          version=0,
          params=ml_dsa_pb2.MlDsaParams(ml_dsa_instance=ml_dsa_pb2.ML_DSA_87),
          key_value=_mldsa87_public_value(),
      ),
  )


def _proto_keys() -> Iterator[Tuple[str, bool, ml_dsa_pb2.MlDsaPrivateKey]]:
  yield ('Basic ML-DSA-65', True, _basic_p65_key())
  yield ('Basic ML-DSA-87', True, _basic_p87_key())


def _output_prefix_types() -> Iterator[Tuple[tink_pb2.OutputPrefixType, bool]]:
  yield (tink_pb2.OutputPrefixType.UNKNOWN_PREFIX, False)
  yield (tink_pb2.OutputPrefixType.TINK, True)
  yield (tink_pb2.OutputPrefixType.LEGACY, False)
  yield (tink_pb2.OutputPrefixType.CRUNCHY, False)
  yield (tink_pb2.OutputPrefixType.RAW, True)


def mldsa_private_keys() -> Iterator[test_key.TestKey]:
  """Returns private test keys for ML-DSA."""
  for name, valid, key_proto in _proto_keys():
    yield test_key.TestKey(
        test_name=name,
        type_url=_PRIVATE_TYPE_URL,
        serialized_value=key_proto.SerializeToString(),
        key_material_type=tink_pb2.KeyData.KeyMaterialType.ASYMMETRIC_PRIVATE,
        valid=valid,
    )

  for output_prefix_type, valid in _output_prefix_types():
    output_prefix_type_name = tink_pb2.OutputPrefixType.Name(output_prefix_type)
    yield test_key.TestKey(
        test_name=f'OutputPrefixType={output_prefix_type_name}',
        type_url=_PRIVATE_TYPE_URL,
        serialized_value=_basic_p65_key().SerializeToString(),
        key_material_type=tink_pb2.KeyData.KeyMaterialType.ASYMMETRIC_PRIVATE,
        output_prefix_type=output_prefix_type,
        valid=valid,
    )


def mldsa_public_keys() -> Iterator[test_key.TestKey]:
  """Returns public test keys for ML-DSA."""
  for name, valid, key_proto in _proto_keys():
    yield test_key.TestKey(
        test_name=name,
        type_url=_PUBLIC_TYPE_URL,
        serialized_value=key_proto.public_key.SerializeToString(),
        key_material_type=tink_pb2.KeyData.KeyMaterialType.ASYMMETRIC_PUBLIC,
        valid=valid,
    )

  for output_prefix_type, valid in _output_prefix_types():
    output_prefix_type_name = tink_pb2.OutputPrefixType.Name(output_prefix_type)
    yield test_key.TestKey(
        test_name=f'OutputPrefixType={output_prefix_type_name}',
        type_url=_PUBLIC_TYPE_URL,
        serialized_value=_basic_p65_key().public_key.SerializeToString(),
        key_material_type=tink_pb2.KeyData.KeyMaterialType.ASYMMETRIC_PUBLIC,
        output_prefix_type=output_prefix_type,
        valid=valid,
    )
